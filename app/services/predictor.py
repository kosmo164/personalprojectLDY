'''
- 역할 : 파이썬의 대표격인 머신러닝.데이터분석 라이브러리인 Scikit-learn(사이킷런)에서
    선형 회귀 모듈을 가져옴
- 의미 : 과거의 데이터를 바탕으로 데이터들을 가장 잘 대변하는 하나의 직잔하는 '추세선(직선)'
    을 그리기 위한 수학적 알고리즘 엔진을 탑재하는 것        
'''
from sklearn.linear_model import LinearRegression
'''
- 역할 : 오라클DB에 접근하는 전역 통신 모듈을 연결
- 의미 : 외귀 연산을 돌리려면 X축에 쓸 시간 데이터와 Y축에 쓸 주가 데이터가 필요하므로,
    DB에서 이를 안전하게 쿼리해오기 위해 연동
'''
from app.db import repository

# 특정 종목의 코드(srtn_cd)를 입력받아 예측값들을 담은 파이썬 딕셔너리(dict)형태로 결과를 반환 
def predict_future_prices(srtn_cd: str) -> dict:
    """단순 선형회귀로 3/6/12개월 뒤 가격의 등락률(%)을 추정."""
    
    '''
    데이터 확보 및 최소 조건 검증
    - 데이터조회 : repository로부터 회귀 연산용 데이터를 가져와 판다스 데이터프레임(df)에 담음. 
        앞서 레포지토리 코드에서 설계했던 종가(clpr)와 주말, 공휴일이 제거된 순차적인 인덱스(day_seq)
        데이터가 넘어옴
    - 표본데이터 최소조건 설정 : 만약 상장된 지 얼마 안 되었거나 DB에 쌓인 주가 데이터가 30일 미만
        (len(df) < 30)이라면, 추세선을 그리기 위한 표본이 너무 부족해 수학적 왜곡이 생김. 따라서
        이 경우에는 무리하게 예측하지 않고 안전하게 모두 0%로 리턴하고 종료    
    '''
    try:
        df = repository.get_close_series_for_regression(srtn_cd)

        if len(df) < 30:
            return {"p_3m": 0, "p_6m": 0, "p_12m": 0}

        '''
        머신러닝 모델 학습데이터 가공(x, y)
        - X(독립변수-원인) : 머신러닝 모델이 학습할 시간 축 데이터. 사이킷런의 LinearRegression모델은 
            x데이터로 2차원배열(행렬)구조를 요구하기 때문에, .reshape(-1, 1)을 통해 1열짜리 세로 배열로
            형태를 간제로 변환(정형화)함.
        - y(종속변수-결과) : 예측하고자 하는 타겟인 실제 주가(종가)배열    
        '''
        X = df["day_seq"].values.reshape(-1, 1)
        y = df["clpr"].values

        '''
        모델생성 및 학습(fit)
        - 건물을 짓듯 선형 회귀 모델 객체(model)를 선언
        - .fit(x, y) : 머신러닝 학습 명령어. 과거의 시간축(x)과 주가(y)를 대조하며 '이 주식은 시간이 지남에
            따라 하루에 평균적으로 몇 원씩 오르거나 내리는 추세를 가지고 있는가?'에 대한 최적의 방정식 기울기와
            절편을 컴퓨터가 스스로 기록(학습)함. 
            ※ 절편 : 수학적으로 평면 위에 직교좌표계가 정의되어 있을 때, 함수 또는 관계의 그래프가 좌표축과 만나는 점. 
                특정 변수의 값이 0이 되기 때문에 근(根)[1] 또는 영점(零點)이라고도 함.
        '''
        model = LinearRegression()
        model.fit(X, y)
        
        '''
        미래시점 타임라인 설정 및 가격예측(predict)
        - 현재시점추출 : 배열의 가장 마지막값([-1])을 조회해, 데이터상의 가장 최신 영업일 숫자(current_day)와
            현재 종가(current_price)를 확보
        - 미래예측 : 학습이 완료된 추세선 공식(model.predict)에 대한 현재 날짜 인덱스로부터 각각 90일뒤, 180일뒤,
            360일뒤의 미래 숫자를 대입하여 컴퓨터가 추정한 미래 가격들을 뽑아냄.(입력값은 규칙에 맞춰 2차원 리스트형태
            [[...]]로 전달)    
        '''
        current_day = X[-1][0]
        current_price = y[-1]

        pred_3m = model.predict([[current_day + 90]])[0]
        pred_6m = model.predict([[current_day + 180]])[0]
        pred_12m = model.predict([[current_day + 365]])[0]

        '''
        현재가 대비 등락률(%)계산 및 결과 반환
        - 프론트엔드 대시보드 화면에 금액(예:72,300원)을 그대로 뿌려주는 것보다 현재가 대비 몇 %상승/하락할지 
            등락률 지표로 주는 것이 직관적임.
        - 수학공식 '미래예측가-현재가/현재가 X 100'을 수행하여 수익률 지표로 가공한 뒤, 보기 좋게 소수점 둘째
            자리까지 반올림(round(..., 2))하여 반환함. 
        '''
        return {
            "p_3m": round(((pred_3m - current_price) / current_price) * 100, 2),
            "p_6m": round(((pred_6m - current_price) / current_price) * 100, 2),
            "p_12m": round(((pred_12m - current_price) / current_price) * 100, 2),
        }
    
    # 방어적 예외처리
    # 예측 연산 중에 혹시 모를 에러(데이터누락, 수학적발산 등)가 터지더라도 전체 시스템이 멈추지 않게 붙잡아두고, 로그를
    # 남긴 후 기본값인 0을 던져 대시보드가 정상적으로 빈 화면이라도 띄울 수 있게 방어함.  
    except Exception as e:
        print(f"❌ 예측 연산 중 오류 발생 ({srtn_cd}): {str(e)}")
        return {"p_3m": 0, "p_6m": 0, "p_12m": 0}
    
'''
알고리즘의 특징과 한계
- 이 코드는 선형회귀분석으로 동작하는 코드임. 주식예측에서 선형회귀분석(Linear Regression)은 과거의 가격 데이터를 직선
    그래프로 그려 미래의 주가를 예상하는 통계 기법임. 이 방법은 구현이 간단하여 주식과 변수 간의 관계를 쉽게 이해하는데
    유리함. 다만 주식시장의 특성상 단순 선형회귀는 과거의 전체적인 장기 우상향/우하향 기조(기울기)만 반영함. 하루 뒤 
    악재나 단기 변동성파동(사이클)을 맞추는 용도로는 한계가 있으므로 거시적인 추세 가이드 지표로 해석하고 활용하는 것이
    바람직함.
'''
    
    
